defmodule NutsApi.Content do
  @moduledoc """
  The Content context.
  """

  import Ecto.Query, warn: false
  alias NutsApi.Repo

  alias NutsApi.Content.Post
  alias NutsApi.Content.User

  @doc """
  Returns the list of posts.

  ## Examples

      iex> list_posts()
      [%Post{}, ...]

  """
  def list_posts do
    Repo.all(Post)
  end

  @doc """
  Gets a single post.

  Raises `Ecto.NoResultsError` if the Post does not exist.

  ## Examples

      iex> get_post!(123)
      %Post{}

      iex> get_post!(456)
      ** (Ecto.NoResultsError)

  """
  def get_post!(id), do: Repo.get!(Post, id)

  @doc """
  Creates a post.

  ## Examples

      iex> create_post(%{field: value})
      {:ok, %Post{}}

      iex> create_post(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_post(attrs \\ %{}) do
    %Post{}
    |> Post.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Creates a posts for a given user.
  """
  def create_post_for_user(%User{} = user, attrs \\ %{}) do
    post = Ecto.build_assoc(user, :posts)

    post
    |> Post.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a post.

  ## Examples

      iex> update_post(post, %{field: new_value})
      {:ok, %Post{}}

      iex> update_post(post, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_post(%Post{} = post, attrs) do
    post
    |> Post.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Post.

  ## Examples

      iex> delete_post(post)
      {:ok, %Post{}}

      iex> delete_post(post)
      {:error, %Ecto.Changeset{}}

  """
  def delete_post(%Post{} = post) do
    Repo.delete(post)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking post changes.

  ## Examples

      iex> change_post(post)
      %Ecto.Changeset{source: %Post{}}

  """
  def change_post(%Post{} = post) do
    Post.changeset(post, %{})
  end

  def preload_user(%Post{} = post) do
    post
    |> Repo.preload(:user)
  end

  def preload_comments(%Post{} = post) do
    post
    |> Repo.preload(:comments)
  end

  def like_post(id) do
    from(p in Post, where: p.id == ^id)
    |> Repo.update_all(inc: [num_likes: 1])
  end

  def unlike_post(id) do
    from(p in Post, where: p.id == ^id)
    |> Repo.update_all(inc: [num_likes: -1])
  end

  def list_posts_by_time() do
    query = 
      from Post,
      order_by: [desc: :inserted_at]

    Repo.all query
  end

